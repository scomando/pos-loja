    private void acncorarEstComboBox() {
        EstabelecimentoJpaController cc = new EstabelecimentoJpaController();
        List<Estabelecimento> E = cc.findEstabelecimentoEntities();
        List<String> estabelecimento = new ArrayList<>();

        for (int i = 0; i < E.size(); i++) {
            estabelecimento.add(E.get(i).getNomeEstabelecimento());
        }

        Object[] estabelecimentosArray = estabelecimento.toArray();
        DefaultComboBoxModel model = new DefaultComboBoxModel(estabelecimentosArray);
        EstjComboBox.setModel(model);
    }

    private Estabelecimento getEst() {
        EstabelecimentoJpaController cc = new EstabelecimentoJpaController();
        List<Estabelecimento> E = cc.findEstabelecimentoEntities();

        for (Estabelecimento E1 : E) {
            if (E1.getNomeEstabelecimento().trim().equalsIgnoreCase(EstjComboBox.getSelectedItem().toString().trim())) {
                return E1;
            }
        }
        return null;
    }

    public boolean userExists() {
        String codigo = LoginCodigoTxtField.getText();
        String password = LoginSenhaTxtField.getText();

        for (int i = 0; i < Ut.size(); i++) {
            if (Ut.get(i).getCodigoUtilizador().equals(codigo.trim()) && Ut.get(i).getSenhaUtilizador().equals(password.trim())) {
                id = Ut.get(i).getIdUtilizador();
                U = Ut.get(i);
                return true;
            }
        }
        return false;
    }

