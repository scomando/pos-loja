    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        // TODO add your handling code here:
        String codigo = LoginCodigoTxtField.getText();
        String password = LoginSenhaTxtField.getText();

        if (codigo.equals("") || password.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!");
        } else if (userExists()) {
            if (getEst().getUtilizadoresList().contains(U)) {
                if (isAdmin()) {
                    Admin admin = new Admin(U, U.getEstabelecimentoId());
                    admin.setVisible(true);
                    dispose();
                } else {
                    User user = new User();
                    user.setVisible(true);
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Nao encontramos o seu utilizador em " + getEst().getNomeEstabelecimento());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Codigo ou Senha Incorrecta!");
        }
    }//GEN-LAST:event_LoginBtnActionPerformed

